package recipebbs;

public class Recipebbs {
	
	private int rebbsID;
	private String rebbsTitle;
	private String userID;
	private String rebbsDate;
	private String rebbsContent;
	private int rebbsAvailable;
	
	
	public int getRebbsID() {
		return rebbsID;
	}
	public void setRebbsID(int rebbsID) {
		this.rebbsID = rebbsID;
	}
	public String getRebbsTitle() {
		return rebbsTitle;
	}
	public void setRebbsTitle(String rebbsTitle) {
		this.rebbsTitle = rebbsTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getRebbsDate() {
		return rebbsDate;
	}
	public void setRebbsDate(String rebbsDate) {
		this.rebbsDate = rebbsDate;
	}
	public String getRebbsContent() {
		return rebbsContent;
	}
	public void setRebbsContent(String rebbsContent) {
		this.rebbsContent = rebbsContent;
	}
	public int getRebbsAvailable() {
		return rebbsAvailable;
	}
	public void setRebbsAvailable(int rebbsAvailable) {
		this.rebbsAvailable = rebbsAvailable;
	}


}
